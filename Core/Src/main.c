void processData(){
	static int16_t leftOut, rightOut;
	for (uint8_t n = 0; n < (BUFFER_SIZE / 2) - 1; n += 2 ){
		leftOut = 32767 * sin(2 * pi * 440 / 48000 * sampleNumber);
		rightOut = leftOut;
		outBufPtr[n] = leftOut;
		outBufPtr[n + 1] = rightOut;
		sampleNumber++;
	}
	dataReadyFlag = 0;

}

